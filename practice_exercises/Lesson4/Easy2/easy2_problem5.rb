There are a number of variables listed below. What are the different types and how do you know which is which? 


We've got a local variable, an instance variable and a class variable. 

Instance variables usually start with an @ symbol 
class variables usually start with two @@
and local variables follow the basic syntax of name followed by an = sign and the value we are assigning to it. 
